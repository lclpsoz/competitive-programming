#include <bits/stdc++.h>
using namespace std;

char arr[] = "AMO FAZER EXERCICIO NO URI";

int main ()
{
	printf ("<%s>\n", arr);
	printf ("<%30s>\n", arr);
	printf ("<%.20s>\n", arr);
	printf ("<%-20s>\n", arr);
	printf ("<%-30s>\n", arr);
	printf ("<%.30s>\n", arr);
	printf ("<%30.20s>\n", arr);
	printf ("<%-30.20s>\n", arr);

	return 0;
}
